export function makeShoutingArray(arr) {
    let shoutingArray = [];
    arr.map(animal => {
        shoutingArray.push({
            name: `${animal.name.toUpperCase()}`,
            type: `${animal.type}`
        });
    });
    return shoutingArray;
}


